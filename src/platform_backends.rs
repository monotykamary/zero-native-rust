/// Detect the current platform at compile time.
pub fn current_backend() -> &'static str {
    if cfg!(target_os = "macos") { "macos" }
    else if cfg!(target_os = "linux") { "linux" }
    else if cfg!(target_os = "windows") { "windows" }
    else { "unknown" }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn mac_platform_creates_with_options() {
        let app_info = AppInfo { app_name: "Test".into(), ..Default::default() };
        let platform = MacPlatform::new(SizeF::new(800.0, 600.0), WebEngine::System, app_info);
        assert_eq!("Test", platform.app_info().app_name);
    }

    #[test]
    fn linux_platform_creates_with_options() {
        let app_info = AppInfo { app_name: "Test".into(), ..Default::default() };
        let platform = LinuxPlatform::new(SizeF::new(800.0, 600.0), WebEngine::System, app_info);
        assert_eq!("Test", platform.app_info().app_name);
    }

    #[test]
    fn windows_platform_creates_with_options() {
        let app_info = AppInfo { app_name: "Test".into(), ..Default::default() };
        let platform = WindowsPlatform::new(SizeF::new(800.0, 600.0), WebEngine::System, app_info);
        assert_eq!("Test", platform.app_info().app_name);
    }

    #[test]
    fn current_backend_matches_os() {
        let backend = current_backend();
        if cfg!(target_os = "macos") { assert_eq!("macos", backend); }
        if cfg!(target_os = "linux") { assert_eq!("linux", backend); }
    }
}
