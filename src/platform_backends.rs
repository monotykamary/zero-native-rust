use crate::geometry::{RectF, SizeF};
use crate::platform::*;
use crate::security;
use crate::policy_values;

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Error {
    CallbackFailed,
    CreateFailed,
    FocusFailed,
    CloseFailed,
}

pub struct MacPlatform {
    app_info: AppInfo,
    surface_value: Surface,
    web_engine: WebEngine,
}

impl MacPlatform {
    pub fn new(size: SizeF, web_engine: WebEngine, app_info: AppInfo) -> Self {
        Self {
            app_info,
            surface_value: Surface { id: 1, size, scale_factor: 1.0 },
            web_engine,
        }
    }

    pub fn surface(&self) -> &Surface { &self.surface_value }
    pub fn app_info(&self) -> &AppInfo { &self.app_info }
    pub fn web_engine(&self) -> WebEngine { self.web_engine }
}

